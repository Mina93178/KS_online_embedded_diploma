#include <stdio.h>
#include <stdlib.h>
void flush(void){
	fflush(stdin);
	fflush(stdout);
}
int main(void) {
	float User_Input_1=0,User_Input_2=0;
	printf("Enter Value of a:");
	flush();
	scanf("%f",&User_Input_1);
	printf("Enter Value of b:");
	flush();
	scanf("%f",&User_Input_2);
	User_Input_1=User_Input_1+User_Input_2;
	User_Input_2 = User_Input_1-User_Input_2;
	User_Input_1=User_Input_1-User_Input_2;
	printf("After swapping, value of a=%.2f\nAfter swapping, value of b=%.2f\n",User_Input_1,User_Input_2);
	return 0;
}

